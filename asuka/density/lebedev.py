def _gen_oh(code: int, a: float, b: float, v: float) -> tuple[np.ndarray, np.ndarray]:
    """Generate an OH-symmetry orbit for the Lebedev grids.

    Parameters
    ----------
    code
        Symmetry orbit code (1..6) as in Laikov's generator.
    a, b
        Orbit parameters (meaning depends on ``code``).
    v
        Weight per generated point (normalized; the full grid sums to 1).
    """
    code = int(code)
    a = float(a)
    b = float(b)
    v = float(v)

    if code == 1:
        base = np.array([1.0, 0.0, 0.0], dtype=np.float64)
    elif code == 2:
        aa = 1.0 / math.sqrt(2.0)
        base = np.array([0.0, aa, aa], dtype=np.float64)
    elif code == 3:
        aa = 1.0 / math.sqrt(3.0)
        base = np.array([aa, aa, aa], dtype=np.float64)
    elif code == 4:
        bb = math.sqrt(max(1.0 - 2.0 * a * a, 0.0))
        base = np.array([a, a, bb], dtype=np.float64)
    elif code == 5:
        bb = math.sqrt(max(1.0 - a * a, 0.0))
        base = np.array([a, bb, 0.0], dtype=np.float64)
    elif code == 6:
        cc = math.sqrt(max(1.0 - a * a - b * b, 0.0))
        base = np.array([a, b, cc], dtype=np.float64)
    else:
        raise ValueError(f"invalid code {code}")

    # 48 candidates: 6 permutations × 8 sign flips.
    pts = base[_PERMS]  # (6,3)
    pts = (pts[:, None, :] * _SIGNS[None, :, :]).reshape((-1, 3))
    pts = np.unique(pts, axis=0)

    expected = _ORBIT_SIZE[code]
    if pts.shape[0] != expected:  # pragma: no cover
        raise RuntimeError(f"orbit code {code}: got {pts.shape[0]} points, expected {expected}")

    w = np.full((pts.shape[0],), v, dtype=np.float64)
    return pts, w


@functools.lru_cache(maxsize=None)
def lebedev_sphere(order: int) -> tuple[np.ndarray, np.ndarray]:
    """Return Lebedev grid points and weights on the unit sphere.

    Parameters
    ----------
    order
        Number of angular points. Supported values are ``LEBEDEV_ORDERS``.

    Returns
    -------
    dirs : (order, 3)
        Unit vectors.
    w : (order,)
        Weights that sum to ``4π``.
    """
    order = int(order)
    if order not in LEBEDEV_PARAMS:
        raise ValueError(f"Unsupported Lebedev order {order}. Supported: {LEBEDEV_ORDERS}")

    params = LEBEDEV_PARAMS[order]
    dirs = np.empty((order, 3), dtype=np.float64)
    w = np.empty((order,), dtype=np.float64)
    n = 0
    for code, a, b, v in params:
        pts, ww = _gen_oh(int(code), float(a), float(b), float(v))
        m = int(pts.shape[0])
        dirs[n : n + m] = pts
        w[n : n + m] = ww
        n += m
    if n != order:  # pragma: no cover
        raise RuntimeError(f"Lebedev generator produced {n} points, expected {order}")

    # Scale to 4π.
    w = w * (4.0 * math.pi)

    # Prevent accidental in-place modification of cached arrays.
    dirs.setflags(write=False)
    w.setflags(write=False)
    return dirs, w


